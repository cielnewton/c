#include <stdio.h>

int main() {
    // Déclaration et initialisation de variables
    int age = 25;  // Variable entière
    float poids = 65.5;  // Variable flottante
    char lettre = 'A';  // Variable caractère
    
    // Affichage des valeurs des variables
    printf("Age: %d\n", age);  // %d pour afficher un entier
    printf("Poids: %.2f kg\n", poids);  // %.2f pour afficher un float avec 2 décimales
    printf("Lettre: %c\n", lettre);  // %c pour afficher un caractère
    
    return 0;
}
